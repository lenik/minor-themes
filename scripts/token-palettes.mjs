/** @param {TokenPalette} palette */
function resolveTokenPalette(palette) {
  /** @type {Record<keyof TokenPalette, string>} */
  const resolved = {};
  for (const [key, value] of Object.entries(palette)) {
    resolved[key] = parseColor(value);
  }
  return resolved;
}

